package com.iss.day02;

public class Demo03 {
    public static void main(String[] args) {
        int num = 5 * 7;
        switch (num) {
            case 10:
                System.out.println("10 符合");break;
            case 20:
                System.out.println("20 符合");break;
            case 30:
                System.out.println("30 符合");break;
            default:
                System.out.println("无符合的值");
        }
    }


}
